import React, { useState, useEffect } from 'react';
import { useTheme } from '../ThemeContext';
import './MapSearch.css';

const MapSearch = ({ onPlaceSelect, onClose }) => {
    const { theme } = useTheme();
    const [searchQuery, setSearchQuery] = useState('');
    const [selectedCity, setSelectedCity] = useState('');
    const [places, setPlaces] = useState([]);
    const [loading, setLoading] = useState(false);
    const [error, setError] = useState('');
    const [activeTab, setActiveTab] = useState('search');
    const [hotCities, setHotCities] = useState([]);
    const [cityList, setCityList] = useState({});
    const [showCitySelector, setShowCitySelector] = useState(false);

    useEffect(() => {
        fetchHotCities();
    }, []);

    const fetchHotCities = async () => {
        try {
            const response = await fetch('/api/map/hot-cities');
            const data = await response.json();
            if (data.success) {
                setHotCities(data.data);
            }
        } catch (err) {
            console.error('Ëé∑ÂèñÁÉ≠Èó®ÂüéÂ∏ÇÈîôËØØ:', err);
        }
    };

    const fetchCityList = async () => {
        try {
            const response = await fetch('/api/map/cities');
            const data = await response.json();
            if (data.success) {
                setCityList(data.data);
            }
        } catch (err) {
            console.error('Ëé∑ÂèñÂüéÂ∏ÇÂàóË°®ÈîôËØØ:', err);
        }
    };

    const searchPlaces = async () => {
        if (!searchQuery.trim()) {
            setError('ËØ∑ËæìÂÖ•ÊêúÁ¥¢ÂÖ≥ÈîÆËØç');
            return;
        }

        setLoading(true);
        setError('');
        try {
            const params = new URLSearchParams({
                keyword: searchQuery,
                city: selectedCity,
                page: 1,
                pageSize: 20
            });

            const response = await fetch(`/api/map/search?${params}`);
            const data = await response.json();
            if (data.success) {
                setPlaces(data.data);
            } else {
                setError('ÊêúÁ¥¢Â§±Ë¥•');
            }
        } catch (err) {
            setError('ÁΩëÁªúÈîôËØØÔºåËØ∑Á®çÂêéÈáçËØï');
            console.error('ÊêúÁ¥¢Âú∞ÁÇπÈîôËØØ:', err);
        } finally {
            setLoading(false);
        }
    };

    const getNearbyPlaces = async (location) => {
        setLoading(true);
        setError('');
        try {
            const params = new URLSearchParams({
                location: location,
                radius: 3000,
                page: 1,
                pageSize: 20
            });

            const response = await fetch(`/api/map/nearby?${params}`);
            const data = await response.json();
            if (data.success) {
                setPlaces(data.data);
            } else {
                setError('Ëé∑ÂèñÂë®ËæπÂú∞ÁÇπÂ§±Ë¥•');
            }
        } catch (err) {
            setError('ÁΩëÁªúÈîôËØØÔºåËØ∑Á®çÂêéÈáçËØï');
            console.error('Ëé∑ÂèñÂë®ËæπÂú∞ÁÇπÈîôËØØ:', err);
        } finally {
            setLoading(false);
        }
    };

    const handleSearch = (e) => {
        e.preventDefault();
        if (activeTab === 'search') {
            searchPlaces();
        }
    };

    const handlePlaceSelect = (place) => {
        onPlaceSelect({
            ...place,
            source: 'map'
        });
        onClose();
    };

    const handleCitySelect = (city) => {
        setSelectedCity(city.name);
        setShowCitySelector(false);
    };

    const handleHotCityClick = (city) => {
        setSelectedCity(city.name);
        setActiveTab('search');
    };

    const handleNearbySearch = () => {
        const defaultLocation = '116.397428,39.90923';
        getNearbyPlaces(defaultLocation);
    };

    const getPlaceTypeIcon = (type) => {
        if (type.includes('È£éÊôØÂêçËÉú')) return 'üèõÔ∏è';
        if (type.includes('È§êÈ•Æ')) return 'üçΩÔ∏è';
        if (type.includes('Ë¥≠Áâ©')) return 'üõçÔ∏è';
        if (type.includes('‰ΩèÂÆø')) return 'üè®';
        if (type.includes('‰∫§ÈÄö')) return 'üöá';
        if (type.includes('Â®±‰πê')) return 'üéÆ';
        return 'üìç';
    };

    const formatDistance = (distance) => {
        if (!distance) return '';
        if (distance < 1000) {
            return `${distance}Á±≥`;
        } else {
            return `${(distance / 1000).toFixed(1)}ÂÖ¨Èáå`;
        }
    };

    return (
        <div className={`map-search-modal ${theme}`}>
            <div className="map-search-content">
                <div className="map-search-header">
                    <h2>Âú∞ÂõæÊêúÁ¥¢</h2>
                    <button className="close-btn" onClick={onClose}>√ó</button>
                </div>

                <div className="search-tabs">
                    <button 
                        className={`tab-btn ${activeTab === 'search' ? 'active' : ''}`}
                        onClick={() => setActiveTab('search')}
                    >
                        ÊêúÁ¥¢Âú∞ÁÇπ
                    </button>
                    <button 
                        className={`tab-btn ${activeTab === 'hot' ? 'active' : ''}`}
                        onClick={() => setActiveTab('hot')}
                    >
                        ÁÉ≠Èó®ÂüéÂ∏Ç
                    </button>
                    <button 
                        className={`tab-btn ${activeTab === 'nearby' ? 'active' : ''}`}
                        onClick={() => setActiveTab('nearby')}
                    >
                        Âë®ËæπÊêúÁ¥¢
                    </button>
                </div>

                {activeTab === 'search' && (
                    <form onSubmit={handleSearch} className="search-form">
                        <div className="city-selector">
                            <button 
                                type="button"
                                className="city-btn"
                                onClick={() => {
                                    fetchCityList();
                                    setShowCitySelector(!showCitySelector);
                                }}
                            >
                                {selectedCity || 'ÈÄâÊã©ÂüéÂ∏Ç'}
                            </button>
                            {showCitySelector && (
                                <div className="city-dropdown">
                                    {Object.entries(cityList).map(([region, cities]) => (
                                        <div key={region} className="region-group">
                                            <h4>{region}</h4>
                                            <div className="city-grid">
                                                {cities.map(city => (
                                                    <button
                                                        key={city.code}
                                                        className="city-item"
                                                        onClick={() => handleCitySelect(city)}
                                                    >
                                                        {city.name}
                                                    </button>
                                                ))}
                                            </div>
                                        </div>
                                    ))}
                                </div>
                            )}
                        </div>
                        <input
                            type="text"
                            value={searchQuery}
                            onChange={(e) => setSearchQuery(e.target.value)}
                            placeholder="ËæìÂÖ•Âú∞ÁÇπÂêçÁß∞..."
                            className="search-input"
                        />
                        <button type="submit" className="search-btn" disabled={loading}>
                            {loading ? 'ÊêúÁ¥¢‰∏≠...' : 'ÊêúÁ¥¢'}
                        </button>
                    </form>
                )}

                {activeTab === 'hot' && (
                    <div className="hot-cities">
                        <h3>ÁÉ≠Èó®ÂüéÂ∏Ç</h3>
                        <div className="city-grid">
                            {hotCities.map(city => (
                                <button
                                    key={city.code}
                                    className="hot-city-btn"
                                    onClick={() => handleHotCityClick(city)}
                                >
                                    {city.name}
                                </button>
                            ))}
                        </div>
                    </div>
                )}

                {activeTab === 'nearby' && (
                    <div className="nearby-section">
                        <h3>Âë®ËæπÊêúÁ¥¢</h3>
                        <p>ÊêúÁ¥¢ÊÇ®ÂΩìÂâç‰ΩçÁΩÆÂë®ËæπÁöÑÂú∞ÁÇπ</p>
                        <button 
                            className="nearby-btn"
                            onClick={handleNearbySearch}
                            disabled={loading}
                        >
                            {loading ? 'ÊêúÁ¥¢‰∏≠...' : 'ÂºÄÂßãÊêúÁ¥¢'}
                        </button>
                    </div>
                )}

                {error && (
                    <div className="error-message">
                        {error}
                    </div>
                )}

                <div className="places-list">
                    {loading ? (
                        <div className="loading">ÊêúÁ¥¢‰∏≠...</div>
                    ) : places.length > 0 ? (
                        places.map((place) => (
                            <div key={place.id} className="place-item" onClick={() => handlePlaceSelect(place)}>
                                <div className="place-icon">
                                    {getPlaceTypeIcon(place.type)}
                                </div>
                                <div className="place-info">
                                    <h4>{place.name}</h4>
                                    <p className="place-address">{place.address}</p>
                                    <div className="place-details">
                                        <span className="place-type">{place.type}</span>
                                        {place.distance && (
                                            <span className="place-distance">
                                                {formatDistance(place.distance)}
                                            </span>
                                        )}
                                        {place.rating > 0 && (
                                            <span className="place-rating">
                                                ‚≠ê {place.rating}
                                            </span>
                                        )}
                                    </div>
                                    {place.tel && (
                                        <p className="place-tel">üìû {place.tel}</p>
                                    )}
                                </div>
                            </div>
                        ))
                    ) : (
                        <div className="no-results">
                            {activeTab === 'search' && 'ËØ∑ËæìÂÖ•ÂÖ≥ÈîÆËØçÊêúÁ¥¢'}
                            {activeTab === 'hot' && 'ËØ∑ÈÄâÊã©ÁÉ≠Èó®ÂüéÂ∏Ç'}
                            {activeTab === 'nearby' && 'ÁÇπÂáªÂºÄÂßãÊêúÁ¥¢Âë®ËæπÂú∞ÁÇπ'}
                        </div>
                    )}
                </div>

                <div className="source-info">
                    <p>Êï∞ÊçÆÊù•Ê∫ê: È´òÂæ∑Âú∞Âõæ</p>
                    <p>Ê≥®ÊÑè: ËØ∑ÈÅµÂÆàÁõ∏ÂÖ≥‰ΩøÁî®Êù°Ê¨æ</p>
                </div>
            </div>
        </div>
    );
};

export default MapSearch; 